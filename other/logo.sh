#!/bin/bash

# Функция подсветки "█" блоков
function print_colored() {
  local b=$'\033[34m'
  local m=$'\033[35m'
  local r=$'\033[0m'
  echo "${b}$(echo "$1" | sed -E "s/(█+)/${m}\1${b}/g")${r}"
}

